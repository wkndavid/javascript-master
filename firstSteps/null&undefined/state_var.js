// Reset de variáveis: Pode ser usado para "limpar" o valor de uma variável.

// Em resumo, undefined é frequentemente usado em contextos onde a ausência de valor ocorre naturalmente, como variáveis não inicializadas ou acessos a propriedades não definidas, enquanto null é usado quando o programador deseja indicar explicitamente que uma variável não contém um valor válido ou que uma função não encontrou um resultado. Cada um tem seu uso apropriado em diferentes contextos de programação.
